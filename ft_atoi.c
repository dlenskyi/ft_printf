#include "ft_printf.h"

int		ft_atoi(const char *s)
{
	int		i;
	int		n;
	int		sign;

	i = 0;
	sign = 1;
	n = 0;
	if (!s)
		return (0);
	while (s[i] == ' ' || (s[i] >= 8 && s[i] <= 14))
		i++;
	if (s[i] == '+')
		i++;
	if (s[i] == '-')
	{
		if (s[i - 1] == '+')
			return (0);
		sign = -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
		n = (n * 10) + (s[i++] - '0');
	return (n * sign);
}
